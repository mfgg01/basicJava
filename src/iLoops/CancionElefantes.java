package iLoops;

public class CancionElefantes {
    public static void main(String[] args) {
        int numElefantes = 1;
        while (numElefantes <= 100) {
            System.out.println(numElefantes + " elefante(s) se columpiaba(n) sobre la tela de una araña,");
            System.out.println("como veía(n) que resistia(n), fueron a llamar a otro elefante...");
            numElefantes++;
        }
    }
}